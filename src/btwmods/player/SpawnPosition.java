package btwmods.player;

public class SpawnPosition {
	public final int dimension;
	public final int x;
	public final int y;
	public final int z;
	
	public SpawnPosition(int dimension, int x, int y, int z) {
		this.dimension = dimension;
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
